
console.log(" Saludar 100 veces");
for (let i = 1; i <= 100; i++) {
  console.log(` ${i} saludos marcos`);
}


console.log(" Números del 5 al 25 ");
for (let i = 5; i <= 25; i++) {
  console.log(`Número: ${i}`);
}


console.log("Números del 100 al 0 ");
for (let i = 100; i >= 0; i--) {
  console.log(`Número: ${i}`);
}


console.log("Cadena con los números del 1 al 10 ");
let cadena = ""; 
for (let i = 1; i <= 10; i++) {
  cadena += i; 
}
console.log(`Cadena final: ${cadena}`);
